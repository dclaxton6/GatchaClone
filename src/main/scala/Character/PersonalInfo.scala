package Character

class PersonalInfo (name: String, age : Int, height: Int, favorite: String,
                    affliation: String, relationships: String,
                   origin: String, reference: String){
  val Name = name
  val Age = age
  val Height = height
  val Favorite = favorite
  val Affliation = affliation
  val Relationships = relationships
  val Origin = origin
  val Reference = reference
}
